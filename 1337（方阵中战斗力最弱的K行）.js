/**
 * @param {number[][]} mat
 * @param {number} k
 * @return {number[]}
 */
var kWeakestRows = function (mat, k) {
    let nums = [];
    for (let i = 0; i < mat.length; i++) {
        let number = 0;
        for (let j = 0; j < mat[i].length; j++) {
            number += mat[i][j];
        }
        nums[i] = [i, number];
    }
    nums.sort(function (a, b) {
        if (a[1] === b[1]) {
            return a[0] - b[0];
        }
        return a[1] - b[1];
    });
    let list = [];
    for (let i = 0; i < k; i++) {
        list.push(nums[i][0]);
    }
    return list;
};


